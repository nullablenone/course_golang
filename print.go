package main

import "fmt"

func main() {
	nama := "muhamad yesa"
	umur := 17
	uang := 18.2
	is_moring := false

	fmt.Printf("nama saya %s \n", nama)
	fmt.Printf("umur saya %d \n", umur)
	fmt.Printf("uang saya %f \n", uang)
	fmt.Printf("is_morning %t \n", is_moring)
	fmt.Printf("coba %T \n", is_moring)
	fmt.Printf("is_morning %v \n", is_moring)
}
